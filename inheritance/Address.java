package inheritance;

public class Address {
	String house_Name;//instance variables or global variables 
	int house_No;
	String city;
	String state;
	String country;
	public Address(String house_Name, int house_No, String city, String state, String country) {
		this.house_Name = house_Name;
		this.house_No = house_No;
		this.city = city;
		this.state = state;
		this.country = country;
	}
	
	
	}